"use client";
import { create } from "zustand";
import { persist } from "zustand/middleware";
import { useEffect, useState } from "react";

export type Lang = "ru" | "kk";

type LangState = { lang: Lang; setLang: (l: Lang) => void };
export const useLang = create<LangState>()(
  persist((set) => ({ lang: "ru", setLang: (lang) => set({ lang }) }), { name: "dush-lang" }),
);

// До гидрации отдаём ru (совпадает с SSR), чтобы не было hydration mismatch.
export function useLangHydrated(): Lang {
  const lang = useLang((s) => s.lang);
  const [h, setH] = useState(false);
  useEffect(() => setH(true), []);
  return h ? lang : "ru";
}

